package demo;

public class ListNode {

    public int val;//数据
    public ListNode next;//指针

    public ListNode(int val) {
        this.val = val;
    }

    public int getval() {
        return val;
    }

    public void setval(int val) {
        this.val = val;
    }

    public ListNode getNext() {
        return next;
    }

    public void setNext(ListNode next) {
        this.next = next;
    }
}
